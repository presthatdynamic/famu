                                    <p>

									I was born in 1964 in Zorleni-Vaslui Romania to a Christian family. I graduate Marine High School in Galati. In 1985 I started traveling and visited over 40 countries. Having a strong desire to change my spiritual and financial life along with having a desire to help the poor, I ran off the ship I was traveling with in 1993 and remained in Canada. That same year, while in Canada, I met a Romanian girl who was visiting her sister.<br/><br/>

									One year later we got married. Although we both came from Christian families, neither of us were living the way God wanted us to. At that time I felt God telling me to seek Him and know Him. I felt the need to stop living in a form of religion and start a personal relationship of love with God the Father, the Son and the Holy Spirit. During this time, God has blessed us with four children, two boys and two girls.<br/><br/>   

									In our search for God, something miraculous had happened. My wife was seeking and thirsting after God, and God had spoken to her. God filled her with abundant love, a passion and flame burning for God and those lost in the world. Following this supernatural encounter her life was transformed completely. God had then called her to take many days of fast and pray for different situations and countries such as Romania and Africa. In 2007, God called my wife to a 40 day and 40 nights fast with water alone, and to ask God to bring a revival in Romania and all the Romanian communities across the world. I also fasted and prayed with my wife, but ate each night.<br/><br/> 

									Shortly afterwards, God has spoken to us clearly and in detail to go on a mission to Romania. In the first year of mission we visited many cities and God has opened up many doors. With passion and love for this broken world, we visited thirty churches, many prisons, orphanages and the streets telling people about the Word of God. Every year God would speak to my wife and call her to set aside 40 days of fasting to prepare us for a new mission in Romania.<br/><br/>  

									God has opened more doors for missions in Europe and Africa: Uganda, Central African Republic. Kenya, Malawi. With Gods help and the financial support of many Romanian communities we were able to reach many people in the African countries: providing food, clothes, sponsoring children to go to school, orphanages etc.<br/><br/>

									In 2015 we were invited to a large convention in Kampala, Uganda where God has opened a door and helped us understand the needs of the Karamojongo tribe. This tribe was in desperate need of water and food.  We then prayed to see if it’s Gods will for us to help them. Almost immediately God had touched the hearts of the Romanian people and a fund was raised to dig a well. In 2016 God continued to work in His miraculous ways and more money were raised to dig other three wells and in a period of three years now we have 24 water wells in Karamoja and people are now drinking clean water. Praise the Lord.
